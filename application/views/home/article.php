		<div class="article-page-word">
			如果要向一個人表達心意，送花或許是一件優雅且不過時的方式，對於繁忙的都市人來說，燈光燦爛的信義區大街或者皇室戰爭儲值禮包都無法取代活生生的植物帶給人的感受。
			如今科技再一次介入一切，買花送花也不例外，在社交平台霸主Facebook上出現了一款旗艦性的聊天機器人--1-800 flowers.com。正當大家思索著如何用精密的人工智能來創造出逆天的智能機器人時，賣花女率先達陣，在Messenger平台上實現了商業變現，這樣一個簡單的功能甚至登上了富比世雜誌，被認為是最值得一用的聊天機器人。
			以對話為基礎的消費體驗發生在「買花」這件事情上，簡單又富有啟發，消費過程大約5分鐘左右，值得所有Chatbot開發者仔細玩味一番。
			本服務目前只有在美國才能使用。
